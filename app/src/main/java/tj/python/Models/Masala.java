package tj.python.Models;

public class Masala {
    private String id;
    private String shart;
    private String kod;
    private String natijaImg;
    private String title;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getShart() {
        return shart;
    }

    public void setShart(String shart) {
        this.shart = shart;
    }

    public String getKod() {
        return kod;
    }

    public void setKod(String kod) {
        this.kod = kod;
    }

    public String getNatijaImg() {
        return natijaImg;
    }

    public void setNatijaImg(String natijaImg) {
        this.natijaImg = natijaImg;
    }
}
